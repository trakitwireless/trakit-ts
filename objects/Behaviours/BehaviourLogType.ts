
	/// <summary>
	/// The type of logged message.
	/// </summary>
	export enum BehaviourLogType {
		/// <summary>
		/// Used for general information messages.
		/// </summary>
		log,
		/// <summary>
		/// Used for more important messages.
		/// </summary>
		info,
		/// <summary>
		/// Used for warnings.
		/// </summary>
		warn,
		/// <summary>
		/// Used for errors.
		/// </summary>
		err,
	}