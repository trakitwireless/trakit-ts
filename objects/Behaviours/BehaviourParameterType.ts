
	/**
	 * Possible data-types given to BehaviourParameters.
	 */
	export enum BehaviourParameterType {
		/**
		 * True or false.
		 */
		booleanean,
		/**
		 * Numeric value (signed double-precision floating point number).
		 */
		number,
		/**
		 * Text.
		 */
		text,
		/**
		 * Object or array literal.
		 */
		json,
	}