
	/**
	 * The four supported types of trackable things.
	 */
	export enum AssetType {
		/**
		 * Generic thing.
		 */
		asset,
		/**
		 * Human (or alien) Person.
		 */
		person,
		/**
		 * A towed vehicle without an engine.
		 */
		trailer,
		/**
		 * A vehicle that moves with its own power.
		 */
		vehicle,
	}